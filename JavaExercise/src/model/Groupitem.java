package model;

public class Groupitem {
	private int groupitemid;
	private int groupid;
	private int authorizationid;
	/**
	 * @return the groupitemid
	 */
	public int getGroupitemid() {
		return groupitemid;
	}
	/**
	 * @param groupitemid the groupitemid to set
	 */
	public void setGroupitemid(int groupitemid) {
		this.groupitemid = groupitemid;
	}
	/**
	 * @return the groupid
	 */
	public int getGroupid() {
		return groupid;
	}
	/**
	 * @param groupid the groupid to set
	 */
	public void setGroupid(int groupid) {
		this.groupid = groupid;
	}
	/**
	 * @return the authorizationid
	 */
	public int getAuthorizationid() {
		return authorizationid;
	}
	/**
	 * @param authorizationid the authorizationid to set
	 */
	public void setAuthorizationid(int authorizationid) {
		this.authorizationid = authorizationid;
	}
}
